package day05_variables;

public class CharExamples {
    public static void main (String [] args){
       char letterOne = 'a';
       char letterTwo = 'Z';
        char letterThree = '9';
        char letterFour = '%';

        System.out.println(letterOne);
        System.out.println(letterTwo);
        System.out.println(letterThree);
        System.out.println(letterFour);
        System.out.println(letterOne + letterTwo); //aZ 97+90=187 in ascii table
        char letter = 74;
        System.out.println(letter); //J in ascii table
        //char letter2 = '75'; //invalid because single quote can hold a single character



        System.out.println(letter); //J in ascii table

    }
}
