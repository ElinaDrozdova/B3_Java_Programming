public class GallonToLiters {
    public static void main(String[] args) {
        int gallon = 10;
        double liter = 37.8541;

        System.out.println(gallon + " gallons equal to " + liter + " liters");
        int multiplication = gallon*2;
        double multiplication2 = liter*2;
        System.out.println(multiplication + " gallons equal to " + multiplication2+ " liters");

    }
}