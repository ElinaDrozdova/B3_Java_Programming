package practice_programming.day04_tasks;

public class PrintDate {
    public static void main(String[] args) {
        int month = 4;
        int day = 9;
        int year = 2016;
        System.out.println (month + "/" + day + "/" + year);
        System.out.println (month + "_" + day + "_" + year);
        System.out.println (month + "-" + day + "-" + year);
        System.out.println (month + "|" + day + "|" + year);
        System.out.println (month + "<>" + day + "<>" + year);
    }

}
