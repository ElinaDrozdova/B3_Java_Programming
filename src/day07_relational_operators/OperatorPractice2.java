package day07_relational_operators;

public class OperatorPractice2 {
    public static void main(String[] args) {
        int a = 40;

        System.out.println(--a); // 39. update and use (print)
        System.out.println(--a); //38
        System.out.println(a--); //38 use and update
        System.out.println(--a); //36.// update and use
        System.out.println(--a); // 35
        System.out.println(a--); //35
        System.out.println(a); //34
    }
}